
principal = 5000
rate_of_interest = 8.5 / 12   
time_in_months = 18

interest_calculated = (principal * rate_of_interest * time_in_months) / 100
total_amount = principal + interest_calculated

print("Principal Amount:$", principal)
print("Rate of interest (per month):", rate_of_interest, "%")
print("Time (in months):", time_in_months)
print("Simple Interest Earned:$", interest_calculated)
print("Total Amount in 18 months:$", total_amount)
